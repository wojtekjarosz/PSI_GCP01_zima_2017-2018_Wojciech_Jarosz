    //funkcja zwracająca sumę w0*x0 + .......+ wn*xn
     public double calculateSum(int[] tab, double[] weights){
        double sum = 0;
        for(int x=0; x < tab.length; x++)
            sum += tab[x] * weights[x];
        return sum;
    }
    //funkcja aktywacji - progowa
    public int activeationFunction(double sum){
        int result;
        if(sum > 0.5) result = 1;
        else result=0;
        return result;
    }
    //funkcja zwracająca nowe wagi, obliczone ze wzoru: w(i+1)=w(i) + wsp_uczenia*(wartość_oczekiwana - suma)*x(i)
    public double[] calculateWeights(int[] table, double learning_rate, double[] weights, double error){
        double[] newWeights = new double[weights.length];
        for(int x=0; x < weights.length;x++)
            newWeights[x] = learning_rate * error * table[x] + weights[x];
        return newWeights;
    }
